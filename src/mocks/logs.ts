export function createLogsPageData(): LogsPageData {
  return {
    stats: {
      todayOperations: operationLogs.length,
      todayExceptions: exceptionLogs.filter((l) => l.status === 'open').length,
      pendingUploads: uploadTasks.filter((t) => t.uploadStatus === 'pending').length,
      failedUploads: uploadTasks.filter((t) => t.uploadStatus === 'failed').length,
      lastUploadTime: iso(18),
    },
    operationLogs,
    exceptionLogs,
    dataFiles,
    uploadTasks,
    exportRecords,
  }
}

export function cloneLogsPageData(data: LogsPageData): LogsPageData {
  return {
    stats: { ...data.stats },
    operationLogs: data.operationLogs.map((l) => ({ ...l })),
    exceptionLogs: data.exceptionLogs.map((l) => ({ ...l })),
    dataFiles: data.dataFiles.map((f) => ({ ...f })),
    uploadTasks: data.uploadTasks.map((t) => ({ ...t })),
    exportRecords: [...data.exportRecords],
  }
}
